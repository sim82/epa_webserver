static int mytqli(double *d, double *e, const int n, double **z)
{
  int     m, l, iter, i, k;
  double  s, r, p, g, f, dd, c, b;
   
  for (i = 2; i <= n; i++)
    e[i - 2] = e[i - 1];

  e[n - 1] = 0.0;

  for (l = 1; l <= n; l++)
    {
      iter = 0;
      do
	{
	  for (m = l; m <= n - 1; m++)
            {
	      dd = fabs(d[m - 1]) + fabs(d[m]);
	      if (fabs(e[m - 1]) + dd == dd)
		break;
	    }

	  if (m != l)
           {
	     assert(iter < 30);
	     
	     g = (d[l] - d[l - 1]) / (2.0 * e[l - 1]);
	     r = sqrt((g * g) + 1.0);
	     g = d[m - 1] - d[l - 1] + e[l - 1] / (g + ((g < 0)?-fabs(r):fabs(r)));/*MYSIGN(r, g));*/
	     s = c = 1.0;
	     p = 0.0;

	     for (i = m - 1; i >= l; i--)
               {
		 f = s * e[i - 1];
		 b = c * e[i - 1];
		 if (fabs(f) >= fabs(g))
		   {
		     c = g / f;
		     r = sqrt((c * c) + 1.0);
		     e[i] = f * r;
		     c *= (s = 1.0 / r);
		   } 
		 else
		   {
		     s = f / g;
		     r = sqrt((s * s) + 1.0);
		     e[i] = g * r;
		     s *= (c = 1.0 / r);
		   }
		 g = d[i] - p;
		 r = (d[i - 1] - g) * s + 2.0 * c * b;
		 p = s * r;
		 d[i] = g + p;
		 g = c * r - b;
		 for (k = 1; k <= n; k++)
		   {
		     f = z[i][k-1];
		     z[i][k-1] = s * z[i - 1][k - 1] + c * f;
		     z[i - 1][k - 1] = c * z[i - 1][k - 1] - s * f;
		   }
	       }

	     d[l - 1] = d[l - 1] - p;
	     e[l - 1] = g;
	     e[m - 1] = 0.0;
	   }
	} 
      while (m != l);
    }

    
 
    return (1);
 }


static void makeEigen(double **_a, const int n, double *d, double *e)
{
  mytred2(_a, n, d, e);
  mytqli(d, e, n, _a);
}

static void initGeneric(const int n, const unsigned int *valueVector, int valueVectorLength,
			double *fracchanges,
			double *ext_EIGN,
			double *EV,
			double *EI,
			double *frequencies,
			double *ext_initialRates,
			double *tipVector,
			int model, 
			boolean useFloat,
			float *EV_FLOAT, 
			float *tipVector_FLOAT)
{
  double 
    **r, 
    **a, 
    **EIGV,
    *initialRates = ext_initialRates, 
    *f, 
    *e, 
    *d, 
    *invfreq, 
    *EIGN,
    *eptr; 
  
  int 
    i, 
    j, 
    k, 
    m, 
    l;  

  r    = (double **)malloc(n * sizeof(double *));
  EIGV = (double **)malloc(n * sizeof(double *));  
  a    = (double **)malloc(n * sizeof(double *));	  
  
  for(i = 0; i < n; i++)
    {
      a[i]    = (double*)malloc(n * sizeof(double));
      EIGV[i] = (double*)malloc(n * sizeof(double));
      r[i]    = (double*)malloc(n * sizeof(double));
    }

  f       = (double*)malloc(n * sizeof(double));
  e       = (double*)malloc(n * sizeof(double));
  d       = (double*)malloc(n * sizeof(double));
  invfreq = (double*)malloc(n * sizeof(double));
  EIGN    = (double*)malloc(n * sizeof(double));

  
  for(l = 0; l < n; l++)		 
    f[l] = frequencies[l];	
  /*assert(initialRates[numRates - 1] == 1.0);	*/
  
  i = 0;
  
  for(j = 0; j < n; j++)	 
    for(k = 0; k < n; k++)
      r[j][k] = 0.0;
  
  for(j = 0; j < n - 1; j++)
    for (k = j+1; k < n; k++)      	  
      r[j][k] = initialRates[i++];         
  
  for (j = 0; j < n; j++) 
    {
      r[j][j] = 0.0;
      for (k = 0; k < j; k++)
	r[j][k] = r[k][j];
    }                         
  
  fracchanges[model] = 0.0;         
  
  for (j = 0; j< n; j++)
    for (k = 0; k< n; k++)
      fracchanges[model] += f[j] * r[j][k] * f[k];             
  
  m = 0;
  
  for(i=0; i< n; i++) 
    a[i][i] = 0;
  
  /*assert(r[n - 2][n - 1] == 1.0);*/
  
  for(i=0; i < n; i++) 
    {
      for(j=i+1;  j < n; j++) 
	{
	  double factor =  initialRates[m++];
	  a[i][j] = a[j][i] = factor * sqrt( f[i] * f[j]);
	  a[i][i] -= factor * f[j];
	  a[j][j] -= factor * f[i];
	}
    }             	        

  makeEigen(a, n, d, e);	   	  	    

  for(i=0; i<n; i++)     
    for(j=0; j<n; j++)       
      a[i][j] *= sqrt(f[j]);
  
  for (i=0; i<n; i++)
    {	  
      if (d[i] > -1e-8) 
	{	      
	  if (i != 0) 
	    {		    
	      double tmp = d[i], sum=0;
	      d[i] = d[0];
	      d[0] = tmp;
	      for (j=0; j < n; j++) 
		{
		  tmp = a[i][j];
		  a[i][j] = a[0][j];
		  sum += (a[0][j] = tmp);
		}
	      for (j=0; j < n; j++) 
		a[0][j] /= sum;
	    }
	  break;
	}
    }
  
  for (i=0; i< n; i++) 
    {
      EIGN[i] = -d[i];
      
      for (j=0; j<n; j++)
	EIGV[i][j] = a[j][i];
      invfreq[i] = 1 / EIGV[i][0]; 
    }                                    
  
  for(l = 1; l < n; l++)
    {
      ext_EIGN[(l - 1)] = EIGN[l]; 
      assert( ext_EIGN[(l - 1)] > 0.0);
    }
  
  eptr = EV;
  
  for(i = 0; i < n; i++)		  
    for(j = 0; j < n; j++)
      {
	*eptr++ = EIGV[i][j];	             	    	     
	if(useFloat)
	  EV_FLOAT[i * n + j] = (float)EIGV[i][j];
      }
  
  for(i = 0; i < n; i++)
    for(j = 1; j < n; j++)
      EI[i * (n - 1) + (j - 1)] = EV[i * n + j] * invfreq[i];  
  
  for(i=0; i < valueVectorLength; i++)
    {
      unsigned int value = valueVector[i];
      
      for(j = 0; j < n; j++)
	tipVector[i * n + j]     = 0;	            

      if(value > 0)
	{		      
	  for (j = 0; j < n; j++) 
	    {	    
	      if ((value >> j) & 1) 
		{
		  int l;
		  for(l = 0; l < n; l++)
		    tipVector[i * n + l] += EIGV[j][l];		      		      		     		      
		}	     		  
	    }	    
	}     
    }

  for(i = 0; i < valueVectorLength; i++)
    {
       for(j = 0; j < n; j++)
	 if(tipVector[i * n + j] > MAX_TIP_EV)
	   tipVector[i * n + j] = MAX_TIP_EV;
    }


  if(useFloat)
    {
      for(i=0; i < valueVectorLength; i++)
	{
	  for(j = 0; j < n; j++)
	    {
	      tipVector_FLOAT[i * n + j] = (float)tipVector[i * n + j];	 
	      assert(tipVector_FLOAT[i * n + j] <= MAX_TIP_EV);
	    }
	}
    }

  for(i = 0; i < n; i++)
    {
      free(EIGV[i]);
      free(a[i]);
      free(r[i]);
    }

  free(r);
  free(a);
  free(EIGV);

  free(f);
  free(e);
  free(d);
  free(invfreq);
  free(EIGN);
}




